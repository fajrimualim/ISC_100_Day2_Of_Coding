
package Fajrin;


public class Hari2 {
    public static void main(String[] args) {
        int integerData = 42;
        double doubleData = 3.14159;
        char charData = 'A';
        boolean booleanData = true;
        byte byteData = 127;
        short shortData = 32767;
        long longData = 1234567890L;
        float floatData = 1.23f;
        
        System.out.println("Data Integer: " + integerData);
        System.out.println("Data Double: " + doubleData);
        System.out.println("Data Char: " + charData);
        System.out.println("Data Boolean: " + booleanData);
        System.out.println("Data Byte: " + byteData);
        System.out.println("Data Short: " + shortData);
        System.out.println("Data Long: " + longData);
        System.out.println("Data Float: " + floatData);
    }
    
}

    